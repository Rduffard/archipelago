export function getProfileShapePolygons(profileId) {
  if (profileId === 'sanguine_archipelago') {
    return SANGUINE_ARCHIPELAGO_POLYGONS
  }

  return EMERALD_VALE_POLYGONS
}

export function getResolvedProfileShapePolygons(profileId, overrides = {}) {
  const baseShapes = getProfileShapePolygons(profileId)

  return Object.fromEntries(
    Object.entries(baseShapes).map(([shapeId, points]) => [
      shapeId,
      overrides?.[shapeId] ?? points,
    ]),
  )
}
